package eng.EXmlSerialization.model;

import java.awt.*;

public class NamedColor {
  private String name;
  private Color color;

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public Color getColor() {
    return color;
  }

  public void setColor(Color color) {
    this.color = color;
  }
}
